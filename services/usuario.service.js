/**
*   @param {Object} body -> Objeto contendo informações (email, nome e senha) do usuário a ser cadastrado.
*/
    async cadastrarUsuario(body) {
        let result = { sucesso: false };

        let user = await usuariosModel.findOne({ email: body.email }).lean();

        if (!user) {
            result.usuario = (await usuariosModel.create(body)).toJSON();
            result.sucesso = true;
            result.emailEnviado = await this._enviaEmailCadastro(body.email, body.nome);
            result.token = this._gerarTokenJWT(result.usuario);
        } 

        return result;
    }

/**
*   @param {Object} body -> Objeto contendo senha e email do usuário.
*/
    async login(body) {

        let user = await usuariosModel.findOne({ email: body.email }).lean();
        let result = { valido: false, token: "", mensagem: "" };

        if (user && user.senha == body.senha) {
            const token = this._gerarTokenJWT(user);
            result.valido = true;
            result.token = token;
        } else {
            result.mensagem = user ? "Senha incorreta" : "Usuário não encontrado";
        }

        return result;
    }
    
/**
*   @param {String} email -> E-mail destino.
*/
    async recuperarSenha(email) {

        let user = await usuariosModel.findOne({ email }).lean();
        let result = { emailEnviado: false, mensagem: "" };

        if (user) {
            const token = this._gerarTokenJWT(user);
            let emailEnviado = await this._enviaEmailRecuperacao(email, user.nome, token);
            result.emailEnviado = emailEnviado;
            result.mensagem = emailEnviado ? "E-mail de recuperação enviado" : "Erro ao enviar E-mail de recuperação. Favor, realizar nova tentativa!";
        } else {
            result.mensagem = "Usuário não encontrado";
        }

        return result;
    }

    /**
    *   @param {String} email -> E-mail do usuário.
    *   @param {String} senha -> Nova senha.
    */
    async alterarSenha(email, senha) {

        let user = await usuariosModel.findOne({ email }).lean();
        let result = { sucesso: false, mensagem: "" };

        if (user) {
            await usuariosModel.updateOne({ email }, { senha })
            result.sucesso = true;
        } else {
            result.mensagem = "Usuário não encontrado";
        }

        return result;
    }


/**
*   @param {Object} usuario -> Registro do Usuario que está logando.
*/
    _gerarTokenJWT(usuario) {
        return jwt.sign(usuario, chaveJwt, { expiresIn: "4h" });
    }

